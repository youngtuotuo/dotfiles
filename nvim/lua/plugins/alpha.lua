return {
  "goolord/alpha-nvim",
  opts = function()
    local header = { opts = { hl = "Type", position = "center" }, type = "text", val = {} }
    math.randomseed(os.time())
    header.val = headers[math.random(1, #headers)]
    local footer = { opts = { hl = "Type", position = "center" }, type = "text", val = {} }

    return {
      layout = {
        { type = "padding", val = math.floor((vim.fn.winheight(0) - #header.val) / 2) },
        header,
        { type = "padding", val = 2 },
        footer,
      },
      section = {
        header = header,
        footer = footer,
      },
    }
  end,
  config = function(_, opts)
    require("alpha").setup(opts)
    vim.api.nvim_create_autocmd("User", {
      group = vim.api.nvim_create_augroup("TuoGroup", { clear = false }),
      pattern = "LazyVimStarted",
      callback = function()
        local stats = require("lazy").stats()
        local footer_val = string.format(
          "󱐋 %d plugins loaded in %.3f ms",
          stats.count,
          stats.startuptime
        )
        opts.section.footer.val = footer_val
        pcall(vim.cmd.AlphaRedraw)
      end,
    })
  end,
}
