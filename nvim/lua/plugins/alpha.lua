return {
  "goolord/alpha-nvim",
  opts = function()
    local header = { opts = { hl = "Normal", position = "center" }, type = "text", val = {} }
    math.randomseed(os.time())
    header.val = headers[math.random(1, #headers)]
    local footer = { opts = { hl = "Normal", position = "center" }, type = "text", val = {} }
    local version = { opts = { hl = "Normal", position = "center" }, type = "text", val = {} }
    local v = vim.version()
    version.val = string.format("Neovim v%d.%d.%d-%s+%s", v.major, v.minor, v.patch, v.prerelease, v.build)

    return {
      layout = {
        { type = "padding", val = math.floor((vim.fn.winheight(0) - #header.val) / 2) },
        header,
        { type = "padding", val = 2 },
        footer,
        { type = "padding", val = 1 },
        version,
      },
      section = {
        header = header,
        footer = footer,
        version = version,
      },
    }
  end,
  config = function(_, opts)
    require("alpha").setup(opts)
    vim.api.nvim_create_autocmd("User", {
      group = vim.api.nvim_create_augroup("TuoGroup", { clear = false }),
      pattern = "LazyVimStarted",
      callback = function()
        local stats = require("lazy").stats()
        local footer_val = string.format("󱐋 %d plugins loaded in %.3f ms", stats.count, stats.startuptime)
        opts.section.footer.val = footer_val
        pcall(vim.cmd.AlphaRedraw)
      end,
    })
  end,
}
